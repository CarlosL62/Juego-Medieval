package com.tictactoeapp;

public class Coordenada {
    private final int x;
    private final int y;

    /**
     * @param x
     * @param y
     */
    public Coordenada(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    

    
}
